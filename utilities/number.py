def cm_to_in(cm):
    return cm / 2.54

def cm_to_m(cm):
    return cm / 100

def in_to_ft(inch):
    return inch / 12

def cm_to_ft(cm):
    return cm / 30.48

def g_to_oz(g):
    return g / 28.35

def g_to_kg(g):
    return g * 1000

def oz_to_lb(oz):
    return oz * 16

def g_to_lb(g):
    return g / 454